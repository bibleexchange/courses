<h2>20. Why is the hook of Daniel different from the other prophetic books?</h2>
<p>In Daniel God is not speaking to His own people.</p>
<h2>21. What were the two languages used in Daniel and why are two languages used?</h2>
<p>What concerns gentiles is written in Aramaic and what is written concerning Jews is written in Hebrew.</p>
<h2>22. What was the first dream Nebuchadnezzar had in detail? There were two main features: 1) an image and 2) a stone.</h2>
<p>23. Tell a little about the second dream that Nebuchadnezzar had?</p>
<p>A great tree grew and was hewn down and only the stump was left for 7 years. </p>
<p>24. For how long was the judgment to last? Seven years.</p>
<h2>25. Describe what happened to Nebuchadnezzar&apos;s heart and the condition of his body?</h2>
<p>His heart was turned as a beast&apos;s heart and his hairs as eagles feathers and his nails like birds claws and his body was wet with the dew of heaven. </p>
<h2>BONUS: What two things brought about the king&apos;s restoration? Repentance and Regeneration. </h2>