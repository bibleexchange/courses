The word &ldquo;doctrine&rdquo; literally means &ldquo;teaching&rdquo; or &ldquo;instruction&rdquo; and may be defined as <i>the fundamental truths of the Bible arranged in systematic form</i>. This study is also commonly called theology&#44; which means literally <i>a reasoned discourse about God</i>. Theology or doctrine may be defined as <i>a science which deals with our knowledge of God and His relations with man</i> 
