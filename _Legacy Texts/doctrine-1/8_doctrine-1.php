@swa=

## Why do we describe theology or doctrine as a &ldquo;science&rdquo;?
@eng=
@swa=

Science is the systematic and logical arrangement of certified facts. <u>We describe theology/doctrine as a science because it consists of facts relating to God and Divine things arranged in a systematic and logical order.</u> 
@eng=
@swa=

## What is the connection between theology and religion?
@eng=
@swa=

Theology is knowledge of God&#44; but religion is the practice of what is known. (John 13:17; James 1:27) You can be a theologian and not at all religious &mdash; i.e.&#44; not having any genuine spiritual experience. 
@eng=
@swa=

## What is the difference between a doctrine and a dogma?
@eng=
@swa=

Dogma is man&apos;s statement of the truth set forth in a creed; while&#44; doctrine is the fundamental truths of the Bible arranged in a systematic form. EXAMPLE: The Roman Catholic Church instructs men to call the pope <i>Holy Father</i> (dogma)&#44; whereas the Bible says in Matthew 23:9 &ldquo;<i>call no man your father&rdquo;. </i>(doctrine). The very context of Christ&apos;s command is addressing the pride and arrogance of men that usurp honor and recognition that is not their own to claim. &ldquo;Our source is not in man&#44; but is in God&rdquo; is the principle Christ is emphasizing. The false doctrine that prefaces this particular dogma is that the pope is literally Christ on the earth. Pope Pius X (1903-1914) is quoted in a sermon on the authority of the pope:  
@eng=
@swa=

<blockquote class='quote'><i> The Pope is not simply the representative of Jesus Christ. On the contrary&#44; he is Jesus Christ Himself&#44; under the veil of the flesh&#44; and who by means of a being common to humanity continues His ministry amongst men ... Does the Pope speak? It is Jesus Christ Who is speaking. Does he teach? It is Jesus Christ Who teaches. Does he confer grace or pronounce an anathema? It is Jesus Christ Himself Who is pronouncing the anathema and conferring the grace. </i></blockquote> 
@eng=
@swa=

The <i>dogma</i> of the Roman Catholic church is based on <i>false doctrine</i> and therefore it is false. Doctrine could be understood as &ldquo;simply what the Bible says&rdquo; and dogma is man&apos;s conclusions drawn from the text of the Scripture. Dogma <i>can be</i> true&#44; but is not necessarily true.  
@eng=
